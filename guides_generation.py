import maya.cmds as cmds

def create_guide(guides_dic):
    # object loop
    for obj in guides_dic:
        # get object dictionary
        obj_dic = guides_dic[obj]

        # create object
        if not obj == 'guides':
            cmds.spaceLocator(n=obj)
        else:
            cmds.createNode('transform', n=obj)

        # parent object
        if obj_dic['parent']:
            cmds.parent(obj, obj_dic['parent'])

        # set object position
        print(obj_dic['t'])
        cmds.xform(
            obj,
            t=obj_dic['t'],
            ws=True,
            a=True
        )
        cmds.xform(
            obj,
            ro=obj_dic['ro'],
            ws=True,
            a=True
        )
        cmds.xform(
            obj,
            s=obj_dic['s'],
            ws=True,
            a=True
        )

